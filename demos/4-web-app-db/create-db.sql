DROP TABLE IF EXISTS CONTACTS
GO

CREATE TABLE CONTACTS
(
	ID           VARCHAR(100) NOT NULL PRIMARY KEY,
	FIRSTNAME    VARCHAR(50),
	LASTNAME     VARCHAR(50),
	AGE          INT,
	FAVORITEBAND VARCHAR(50)
)
GO

CREATE UNIQUE INDEX CONTACTS_ID_UINDEX
	ON CONTACTS (ID)
GO

